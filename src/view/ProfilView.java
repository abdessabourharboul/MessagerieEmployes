    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        User user = getParam();
        Compte compte = user.getCompte();
            try {
                
                int res =compteService.update(compte);
                if(res>0){
                    JOptionPane.showMessageDialog(null, "MODIFICATION TERMINEE", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                    new ProfilView().setVisible(true);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "ERROR", "ERROR INFORMATION", JOptionPane.ERROR_MESSAGE);
            }

    }//GEN-LAST:event_jButton6ActionPerformed

    private User getParam() {
//        Compte compte = new Compte();
        User myUser = (User) Session.getAttribut("user");
        myUser.getCompte().setNom(nomField.getText());
        myUser.getCompte().setPrenom(prenomField.getText());
        myUser.getCompte().setAdresse(adresseField.getText());
        myUser.getCompte().setTelMob(new Long(telField.getText()));
        return myUser;
    }
