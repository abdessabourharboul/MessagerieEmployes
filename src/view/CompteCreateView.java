    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        Compte compte = getParam();
        User user = getParam1();
        try {
            if (compte.getPassword().equals(compte.getConfermationPassword())) {
                compte.setConnexion("horsligne");
                int res = compteService.save(compte);
                
                userService.save(user);

                if (res > 0) {
                    JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC SUCCES", "info", JOptionPane.INFORMATION_MESSAGE);
                    
                    this.setVisible(false);
                    new ConnexionView().setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(null, "LA CONFIRMATION DU MOT DE PASSE EST INCORECTTE", "ereur", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC ERROR", "ereur", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_saveButtonActionPerformed
    private User getParam1() {
        User user = new User();
        user.setId(logField.getText());
        user.setPasword(passwordField.getText());
        user.getCompte().setId(logField.getText());
        return user;
    }

    private Compte getParam() {
        Compte compte = new Compte();
        compte.setId(logField.getText());
        compte.setNom(nomField.getText());
        compte.setPrenom(prenomField.getText());
        compte.setPassword(passwordField.getText());
        compte.setConfermationPassword(confermatioPasswordField.getText());
        compte.setAdresse(adresseArea.getText());
        compte.setRole(roleComboBox.getSelectedItem() + "");
        compte.setTelMob(new Long(telephoneField.getText()));
       
       
        return compte;
    }

