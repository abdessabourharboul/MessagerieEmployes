    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
        try {
           // TODO add your handling code here:

            Message message = getParem();
            if(!contactsComboBox.getSelectedItem().equals("--SELECT CONTACT--")){
                
            
            
            int res = messageService.save(message);
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "MESSAGE ENVOYE AVEC SUCCES", "info", JOptionPane.INFORMATION_MESSAGE);
            }
            this.setVisible(false);
            }
            else {
                JOptionPane.showMessageDialog(null, "Selectioner un contact", "info", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(true);
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "MESSAGE NON ENVOYE", "ereur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_sendButtonActionPerformed

    private Message getParem() {
        Message message = new Message();
        User myUser = (User) Session.getAttribut("user");
        message.getCompte().setId(contactsComboBox.getSelectedItem()+"");
        message.setCorps(inputTextArea.getText());
        message.setIdEmetteur(myUser.getCompte().getId());
        message.setEtatLecture("Non lu");
        
        return message;
    }

